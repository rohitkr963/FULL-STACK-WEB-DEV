let a=5
let b=++a
console.log(a)
console.log(b)



// Step-by-Step Execution:
// Initialization:

// let a = 5;
// Here, a is initialized with the value 5.
// Pre-Increment Operation:

// let b = ++a;
// The ++a operation is a pre-increment operation. This means that a is incremented by 1 before its value is used in the assignment.
// So, a is incremented from 5 to 6.
// Then, this new value of a (6) is assigned to b.
// Output Values:

// console.log(a);
// At this point, a is 6, so this will print 6.
// console.log(b);
// The value of b is also 6 (the value of a after increment), so this will print 6 as well.
// Final Values:
// After executing the code:
// Value of a: 6
// Value of b: 6