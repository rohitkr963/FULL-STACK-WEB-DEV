//power of Number

let a=3
let b=2

let result=a**b;               // result = a^b  mean 3^2 = 9   like power of number

let result2=a**b*2;               // result = a^b  mean 3^2 = 9 ,     9*2=18

console.log(result)
console.log(result2)

// output

// 9
// 18




// Guess the Output:
// The output will be 18.

// Explanation:
// Exponentiation Operator (**):

// In the expression a ** b, the ** operator raises a to the power of b.
// So, 3 ** 2 means 
// 3
// 2
// 3 
// 2
//  , which equals 9.
// Multiplication:

// After calculating 3 ** 2, the result is 9.
// Now, we multiply this result by 2:
// 9 * 2 equals 18.